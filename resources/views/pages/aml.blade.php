                                Anti-Money Laundering (AML) Policy
                            </h5>
                            
<p><strong>What is Money Laundering?</strong></p>
<p><span style="font-weight: 400;">Money laundering is the act of hiding money obtained illegally, so the source appears legitimate. We adhere to strict laws rendering it illegal for us or any of our employees or agents to knowingly engage in or attempt to engage in any activities remotely related to money laundering. Our anti-money laundering policies increase investor protection and client security services, as well as offer safe payment processes.</span></p>
<p><strong>Identification</strong></p>
<p><span style="font-weight: 400;">The first safeguard against money laundering is sophisticated Know-Your-Client (KYC) verification. To ensure compliance with standard AML regulations, we require you to submit the following documentation:</span></p>
<ul>
<li style="font-weight: 400;"><strong>Proof of Identification:</strong><span style="font-weight: 400;">&nbsp;A clear copy of the front and back of your government-issued photo ID, i.e. a valid passport, driver&rsquo;s licence, or national ID card.</span></li>
<li style="font-weight: 400;"><strong>Proof of Residence:</strong><span style="font-weight: 400;">&nbsp;An official document issued within the last 3 months, clearly stating your name and address as registered with TRADEFI. This can be a utility bill (i.e. water, electric, or landline), or bank statement.&nbsp;Please make sure your copy includes:</span></li>
<ul>
<li style="font-weight: 400;"><span style="font-weight: 400;">Your full, legal name</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Your full residential address</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Date of issue (within the last 3 months)</span></li>
<li style="font-weight: 400;"><span style="font-weight: 400;">Name of the issuing authority with an official logo or stamp</span></li>
</ul>
</ul>
<p><strong>Monitoring</strong></p>
<p><span style="font-weight: 400;">We do not accept third-party payments. All deposits must be made in your own name and match the KYC documents submitted. Due to AML regulatory policies, all funds withdrawn must be returned to the exact source from which they were received.</span></p>
<p><strong>Reporting</strong></p>
<p><span style="font-weight: 400;">AML regulation requires us to monitor and track suspicious transactions and report such activities to the relevant law enforcement agencies. We also reserve the right to deny a transfer at any stage if we believe the transfer to be connected in any manner to criminal activity or money laundering.&nbsp;The law prohibits us from informing customers about reports submitted to law enforcement agencies concerning suspicious activity.</span></p>
<p><span style="font-weight: 400;">If you have any inquiries, please contact us via email:&nbsp;</span><a href="mailto:info@tradefi.ng"><span style="font-weight: 400;">info@tradefi.ng</span></a></p>
                            
